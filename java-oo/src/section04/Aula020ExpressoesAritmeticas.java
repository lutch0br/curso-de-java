package section04;

public class Aula020ExpressoesAritmeticas {

	public static void main(String[] args) {
 
		System.out.println("Soma: 4 + 4: " + (4 + 4)  );
		System.out.println("Soma: 4 - 4: " + (4 - 4)  );
		System.out.println("Soma: 4 * 4: " + (4 * 4)  );
		System.out.println("Soma: 4 / 4: " + (4 / 4)  );
		System.out.println("Soma: 4 mod 4: " + (4 % 4)  );
	}

}
